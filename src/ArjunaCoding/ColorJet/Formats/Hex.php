        /**
     * Get distance from the given color using the Delta E method
     * 
     * @param Color $color 
     * 
     * @return float
     */
    public function getDistanceLabFrom($color)
    {
        $lab1 = $this->toCIELab();
        $lab2 = $color->toCIELab();

        $lDiff = abs($lab2->lightness - $lab1->lightness);
        $aDiff = abs($lab2->a_dimension - $lab1->a_dimension);
        $bDiff = abs($lab2->b_dimension - $lab1->b_dimension);
        
        $delta = sqrt($lDiff + $aDiff + $bDiff);
        
        return $delta;
    }
    

    /**
     * Get the closest matching color from the given array of colors
     * 
     * @param array $colors array of integers or Color objects
     * 
     * @return mixed the array key of the matched color
     */
    public function getClosestMatch(array $colors)
    {
        $matchDist = 10000;
        $matchKey = null;
        foreach($colors as $key => $color) {
            if (false === ($color instanceof Hex)) {
                $c = $this->create($color); 
            }
            $dist = $this->getDistanceLabFrom($c);
            if ($dist < $matchDist) {
                $matchDist = $dist;
                $matchKey = $key;
            }
        }
        
        return $matchKey;
    }


