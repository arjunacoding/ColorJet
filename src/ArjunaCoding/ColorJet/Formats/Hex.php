    /**
     * Create a new Hex
     *
     * @param int $hex the hexidecimal value (i.e. 0x000000)
     */
    public function __construct($hex)
    {
        if ($hex > 0xFFFFFF || $hex < 0) {
            throw new InvalidArgumentException(sprintf('Parameter hex out of range (%s)', $hex));
        }

        $this->hex = $hex;
        $this->toSelf = "toHex";
    }

